# Exemplo 1: Demonstação das operações
print("Exemplo 1:")
print("Soma: 10+10 =", 10+10)
print("Subtração: 10-5 =", 10-5)
print("Multiplicação: 10*10 =", 10*10)
print("Divisão: 10/4 =", 10/4)
print("Divisão inteira: 10//4 =", 10//4)
print("Módulo: 10%4 =", 10%4)
print("Potenciação: 10**2 =", 10**2)
print("Parêntesis: 5+2*10 =", 5+2*10, "!= (5+2)*10 =", (5+2)*10, "\n")

# Exemplo 2: Operações com diferentes tipos de dados
print("Exemplo 2:")
print("Repetição: int * str ->", 10 * "Ju")
print("Concatenação: str + str ->", "Juliana " + "tem " + str(21) + " anos.")

