# Aula 5: Operadores Aritmeticos

# Exemplo 1: demonstação das operações
print("Exemplo 1:")
print("Soma: 10+10 =", 10+10)
print("Subtracao: 10-5 =", 10-5)
print("Multiplicacao: 10*10 =", 10*10)
print("Divisao: 10/4 =", 10/4)
print("Divisao inteira: 10//4 =", 10//4)
print("Modulo: 10%4 =", 10%4)
print("Potenciacao: 10**2 =", 10**2)
print("Parentesis: 5+2*10 =", 5+2*10, "!= (5+2)*10 =", (5+2)*10, "\n")

# Exemplo 2: operações com diferentes tipos de dados
print("Exemplo 2:")
print("Repeticao: int * str ->", 10 * "Ju")
print("Concatenacao: str + str ->", "Juliana " + "tem " + str(21) + " anos.")

